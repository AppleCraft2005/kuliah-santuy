#include <stdio.h>

void main() {
    float jARI_JARI,TINGGI,VOLUME,LUAS,KELILING;
    float PHI = 3.142857;

    scanf("%f %f", &jARI_JARI, &TINGGI);
    
    printf("Volume : %.2f\n",VOLUME = PHI * jARI_JARI * jARI_JARI * TINGGI);
    printf("Luas : %.2f\n",LUAS = (2 * PHI * jARI_JARI * jARI_JARI) + (2 * PHI * jARI_JARI * TINGGI));
    printf("Keliling : %.2f\n",KELILING = 2 * PHI * jARI_JARI);

}